    private void getMaxValue() {
        try {
            java.sql.ResultSet rs1 = DB.getdata("SELECT MAX(category_id) FROM product_category");
            if (rs1.next()) {
                //System.out.println("Table not empty");
                maxId = rs1.getInt("max(category_id)");
                //System.out.println(maxCusId);
                catIdLbl.setText(String.valueOf(maxId + 1));
                newId(maxId);
            } else {
                System.out.println("Table Empty");

            }
        } catch (Exception ex) {
            Logger.getLogger(CustomerRegistration.class
                    .getName()).log(Level.SEVERE, null, ex);
        }
    }

    private void newId(int maxValue) {
        newId = maxValue + 1;
    }

    private boolean categoryChecker(String username) {
        String sql = "SELECT * FROM product_category WHERE category_name=?";
        boolean Exist = false;
        try {
            PreparedStatement pstmt = DB.con().prepareStatement(sql);
            pstmt.setString(1, username);
            ResultSet resultSet = pstmt.executeQuery();
            if (resultSet.next()) {
                System.out.println("Category is already taken.");
                Exist = true;
            } else {
                System.out.println("Category is available.");
                Exist = false;

            }
        } catch (Exception ex) {
            Logger.getLogger(UserRegistration.class
                    .getName()).log(Level.SEVERE, null, ex);
        }
        return Exist;
    }
