    @Override
    protected void paintComponent(Graphics grphcs) {
        Graphics2D g2 = (Graphics2D) grphcs;
        g2.setRenderingHint(RenderingHints.KEY_ANTIALIASING, RenderingHints.VALUE_ANTIALIAS_ON);

        // Define start and end colors for the gradient
        // Create a GradientPaint
        GradientPaint gradientPaint = new GradientPaint(0, 0, startColor, 0, getHeight(), endColor);

        // Set the paint to the graphics context
        g2.setPaint(gradientPaint);

        // Fill a rounded rectangle with the gradient paint
        g2.fillRoundRect(0, 0, getWidth(), getHeight(), 15, 15);

        super.paintComponent(grphcs);
    }

