    public void showMessage(String title, String message, int type) {
        switch (type) {
            case 1:
                func.iconSetter(icon, il.doneIcon);
                break;
            case 2:
                func.iconSetter(icon, il.warningIcon);
                break;
            case 3:
                func.iconSetter(icon, il.errorIcon);
                break;
            default:
                func.iconSetter(icon, il.errorIcon);
                break;
        }
        titleLbl.setText(title);
        messageLbl.setText(message);
        setLocationRelativeTo(fram);
        setVisible(true);
    }
