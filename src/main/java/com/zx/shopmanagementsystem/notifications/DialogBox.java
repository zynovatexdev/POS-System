    public void showDialogBox(String title, String message, String messageType) {
        this.setVisible(true);
        if (messageType.equals("1")) {
            func.iconSetter(iconLbl, il.doneIcon);
            messageLbl.setText(message);
            titleLbl.setText(title);
        } else if (messageType.equals("2")) {
            func.iconSetter(iconLbl, il.warningIcon);
            messageLbl.setText(message);
            titleLbl.setText(title);
        } else if (messageType.equals("3")) {
            func.iconSetter(iconLbl, il.errorIcon);
            messageLbl.setText(message);
            titleLbl.setText(title);
        } else {
            func.iconSetter(iconLbl, il.errorIcon);
            messageLbl.setText("Worng Message Type");
            titleLbl.setText("Wrong Message Type");
        }
    }
